def process(table, encounterMethod, areaId, isNatDex = False):
    global newEncounters
    global newConditionMaps
    global currentId

    length = 12
    if encounterMethod == 'walk':
        length = 10

    for timeOfDay in ['morning', 'day', 'night']:
        for slot in range(length):
            line = ''
            conditionLine = ''

            ix = slot * 2
            line += str(currentId) + ',' # id
            line += str(version) + ',' # version
            line += str(areaId) + ',' # location_area_id
            line += str(method[timeOfDay][encounterMethod] + slot) + ',' # encounter_slot_id
            line += str(table[timeOfDay][ix]) + ',' # pokemon_id
            line += str(table[timeOfDay][ix+1]) + ',' # min_level
            line += str(table[timeOfDay][ix+1]) # max_level

            conditionLine += str(currentId) + ','
            conditionLine += str(condition[timeOfDay])

            newEncounters += line + '\n'
            newConditionMaps += conditionLine + '\n'

            if isNatDex:
                conditionLine = ''
                conditionLine += str(currentId) + ','
                conditionLine += str(natDexCondition)
                newConditionMaps += conditionLine + '\n'

            currentId += 1

